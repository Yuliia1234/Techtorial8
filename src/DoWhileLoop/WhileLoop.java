package DoWhileLoop;

public class WhileLoop {
    public static void main(String[] args) {

        int count = 0;//you can start from 0 till ....
        while (count <= 10) {
         //   System.out.println("My name is Julia");
            System.out.println("This is my " + count + " print");
            count++;
        }
        int number = 5;
        while (number > 0) {
            System.out.println("This is my number " + number);
         number--;

        }
    }


}